//! TypeScript type generation binary.
//!
//! Run with `cargo run --bin generate-ts` from the workspace root.
//! Output is written to `generated/types.ts`.

use std::fmt::Write as FmtWrite;
use std::io;

use ts_rs::TS;
use werkstatt_call_shared::{
    CallDetail, CallDirection, CallEvent, CallLogSummary, CallNote, ClientCommand, ClientHello,
    ContactPerson, Customer, CustomerSuggestion, EnrichedCallEvent, LastContact, LinkSource,
    NoteCategory, OpenItem, OpenOrder, PhoneLink, ServerEvent, Vehicle, WireFormat,
};

fn main() -> io::Result<()> {
    let mut out = String::new();
    writeln!(
        out,
        "// Auto-generated by `cargo run --bin generate-ts`. Do not edit manually."
    )
    .ok();
    writeln!(
        out,
        "// Re-generate with: cargo run --bin generate-ts > generated/types.ts"
    )
    .ok();
    writeln!(out).ok();

    macro_rules! emit {
        ($($ty:ty),+ $(,)?) => {{
            $(
                writeln!(out, "{}", <$ty>::decl()).ok();
                writeln!(out).ok();
            )+
        }};
    }

    emit!(
        CallDirection,
        CallEvent,
        Customer,
        CustomerSuggestion,
        Vehicle,
        OpenOrder,
        OpenItem,
        LastContact,
        EnrichedCallEvent,
        NoteCategory,
        CallNote,
        LinkSource,
        ContactPerson,
        PhoneLink,
        WireFormat,
        ClientHello,
        ServerEvent,
        ClientCommand,
        CallLogSummary,
        CallDetail,
    );

    // Trim trailing blank line
    let out = out.trim_end().to_string() + "\n";

    std::fs::create_dir_all("generated")?;
    std::fs::write("generated/types.ts", out)?;
    eprintln!("Written: generated/types.ts");
    Ok(())
}
